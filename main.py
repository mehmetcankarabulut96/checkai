async def call_vlm(image_content: bytes) -> dict:
    """
        GPT-4o (veya Vision destekli başka bir model) kullanarak gerçek VLM analizi yapar.
        Sadece istenen JSON formatında yanıt dönmeye zorlanmıştır.
    """
    # 1. Byte verisini Base64'e çevir
    base64_image = base64.b64encode(image_content).decode('utf-8')
    
    # 2. VLM için katı kurallı System Prompt
    system_prompt = """
    You are an expert forensic image analysis AI. Your task is to detect semantic and logical anomalies that indicate an image is a deepfake or AI-generated.
    Score the following 5 categories from 0.0 (perfectly authentic) to 100.0 (definitely anomalous/fake).
    You MUST return ONLY a valid JSON object with exact following keys and float values:
    {
        "biological_anomaly": float,
        "physical_inconsistency": float,
        "deliberate_obscuration": float,
        "contextual_mismatch": float,
        "synthetic_artifacts": float
    }
    """

    try:
        # 3. Asenkron API Çağrısı (response_format ile JSON garantisi)
        response = await openai_client.chat.completions.create(
            model="gpt-4o-mini", # Maliyet/Hız optimizasyonu için mini veya gpt-4o kullanabilirsin
            response_format={ "type": "json_object" },
            messages=[
                {"role": "system", "content": system_prompt},
                {"role": "user", "content": [
                    {
                        "type": "image_url",
                        "image_url": {
                            "url": f"data:image/jpeg;base64,{base64_image}",
                            "detail": "high" # Yüksek çözünürlüklü analiz için
                        }
                    }
                ]}
            ],
            max_tokens=200,
            temperature=0.0, # Sabit ve tutarlı skorlar için 0 (sıfır) olmalı
            timeout=15.0 # 15 saniyelik timeout, bu süre aşılırsa istisna fırlatır
        )
        
        # response
        usage = response.usage # prompt_tokens, completion_tokens, total_tokens içerir
        raw_json = response.choices[0].message.content
        sub_details = json.loads(raw_json)
        
    except Exception as e:
        raise RuntimeError("VLM_ANALYSIS_FAILED") from e

    # filtreleme ve etiketleme mantığı
    reasons = [key.upper() for key, score in sub_details.items() if float(score) >= 80.0]
    total_score = max([float(v) for v in sub_details.values()])
    
    if not reasons:
        explanation = "No significant semantic anomalies detected. The image appears logically consistent."
    else:
        explanation_parts = [VLM_EXPLANATION_MAP[reason] for reason in reasons if reason in VLM_EXPLANATION_MAP]
        explanation = f"Analysis detected {', '.join(explanation_parts)}. These findings suggest a lack of semantic authenticity."

    return {
        "total_anomaly_score": total_score,
        "anomaly_reasons": reasons,
        "anomaly_explanation": explanation,
        "details": sub_details,
        "prompt_tokens": usage.prompt_tokens,
        "completion_tokens": usage.completion_tokens,
        "openai_request_id": response.id
    }

def decode_image(image_bytes: bytes):
    """
    Decodes raw image bytes into an OpenCV-compatible matrix (NumPy array).

    Safely handles empty payloads and corrupted files. Note that if OpenCV 
    fails to read the bytes, it silently returns None instead of throwing an error.

    Args:
        image_bytes (bytes): The raw binary content of the uploaded file.

    Returns:
        numpy.ndarray | None: The decoded image matrix in BGR format, or 
                              None if the image is empty or corrupted.
    """
    
    if not image_bytes:
        return None
    
    nparr = np.frombuffer(image_bytes, np.uint8)
    return cv2.imdecode(nparr, cv2.IMREAD_COLOR)

def check_resolution(img_matrix, min_width=512, min_height=512) -> dict:
    height, width = img_matrix.shape[:2]
    if width < min_width or height < min_height:
        return {"is_valid": False, "error_code": "LOW_RESOLUTION", "message": f"Resolution too low ({width}x{height}). Minimum required is {min_width}x{min_height}.",
                "recommendation": f"Upload a larger, higher-quality image. The image you provided is too small to analyze accurately. Ensure it is at least {min_width}x{min_height} pixels."}
    return {"is_valid": True}

def check_blur(img_matrix, blur_threshold=35.0) -> dict:
    gray = cv2.cvtColor(img_matrix, cv2.COLOR_BGR2GRAY)
    variance = cv2.Laplacian(gray, cv2.CV_64F).var()
    if variance < blur_threshold:
        return {"is_valid": False, "error_code": "BLURRY_IMAGE", "message": "Image is too blurry for reliable analysis.",
                "recommendation": "The image is out of focus. Please retake the photo with a steady hand, ensuring the subject is sharp and clear."}
    return {"is_valid": True}

def check_exposure(img_matrix, dark_thresh=20, bright_thresh=235) -> dict:
    gray = cv2.cvtColor(img_matrix, cv2.COLOR_BGR2GRAY)
    mean_brightness = np.mean(gray)
    if mean_brightness < dark_thresh:
        return {"is_valid": False, "error_code": "TOO_DARK", "message": "Image is too dark for analysis.",
                "recommendation": "The image is too dark for us to see the details clearly. Please retake the photo in a well-lit area or turn on your flash."}
    if mean_brightness > bright_thresh:
        return {"is_valid": False, "error_code": "TOO_BRIGHT", "message": "Image is overexposed.",
                "recommendation": "The image is overexposed or washed out by harsh lighting. Please retake the photo without a direct glare or try turning off your flash."}
    return {"is_valid": True}

def validate_image_quality(img_matrix) -> dict:
    if img_matrix is None:
        return {"is_valid": False, "error_code": "UNPROCESSABLE_IMAGE", "message": "Image could not be decoded.",
                "recommendation": "Ensure your file is a valid, uncorrupted image and try uploading it again. Resaving the image in a standard format like JPG or PNG often fixes this issue."}
        
    # çözünürlük
    res_check = check_resolution(img_matrix)
    if not res_check.get("is_valid"): return res_check
        
    # pozlama: karanlık ve parlaklık
    exp_check = check_exposure(img_matrix)
    if not exp_check.get("is_valid"): return exp_check
    
    # bulanıklık
    blur_check = check_blur(img_matrix)
    if not blur_check.get("is_valid"): return blur_check
        
    return {"is_valid": True}

async def check_human_face(img_matrix) -> dict:
    """
        Numpy 2.0+ ve en güncel MediaPipe sürümleriyle uyumlu, 
        'image_ptr' hatasını engelleyen modern implementasyon.
    """
    try:    
        # MediaPipe RGB formatı bekler
        img_rgb = cv2.cvtColor(img_matrix, cv2.COLOR_BGR2RGB)
        # KRİTİK ADIM: Numpy dizisinin hafıza düzenini (memory layout) zorla düzeltiyoruz
        # Bu satır, MediaPipe'ın beklediği C-style hafıza dizilimini garanti eder.
        img_rgb = np.ascontiguousarray(img_rgb)

        # MediaPipe Image objesini oluştur
        mp_image = mp.Image(image_format=mp.ImageFormat.SRGB, data=img_rgb)
        
        # CPU bloklanmasını önlemek için asenkron thread kullan
        detection_result = await asyncio.to_thread(face_detector.detect, mp_image)
        face_count = len(detection_result.detections) if detection_result.detections else 0

        if face_count == 0:
            return {"is_valid": False, "error_code": "FACE_NOT_DETECTED", "message": "No valid human face found in the image.", "face_count": 0,
                    "recommendation": "Ensure a human face is clearly visible and well-lit."}
        elif face_count > 1:
            return {"is_valid": False, "error_code": "MULTIPLE_FACES_DETECTED", "message": "Multiple faces detected.", "face_count": face_count,
                    "recommendation": "Upload an image with a single person."}
            
        return {"is_valid": True, "face_count": face_count}
        
    except Exception as e:
        raise RuntimeError("Face detection engine failed") from e

# global dependency function
async def get_auth_user(request: Request, api_key: str = Depends(api_key_header), credentials: HTTPAuthorizationCredentials = Depends(security)):
    """
    Global dependency for handling dual-layer authentication and user profile resolution.

    Authentication Strategy:
    1. API Key Validation (Priority B2B): If an 'X-API-KEY' header is present, it hashes 
       the key and verifies it against the 'api_keys' database table.
    2. JWT Validation (Dashboard Session): Fallback to standard Bearer tokens via Supabase 
       auth. Automatically validates token freshness and ensures email confirmation.

    Security & Business Gating:
    - Profile Lookup: Ensures a valid row exists in the 'profiles' data cluster.
    - Beta Waitlist Check: Rejects requests if 'is_allowed' is false on the profile.

    Returns:
        dict: A unified authentication context dictionary containing:
            - "id": The authenticated user's unique UUID string.
            - "auth_type": "api_key" or "jwt" depending on the client handshake.
            - "is_test": Boolean flag derived from 'sk_test_' prefixes (API key only).
            - "profile": The complete dictionary row from the database 'profiles' table.
            - "provider": The OAuth/identity provider metadata string (JWT only).

    Raises:
        HTTPException: Standardized 'raise_api_error' structures (401, 403, 404, 500) 
                       with distinct error codes for client-side integration tracking.
    """
    
    user_id = None
    auth_info = {}
    request_id = getattr(request.state, "request_id", None)
    target_endpoint = request.url.path

    # get user id from provided api-key
    if api_key:
        api_key = api_key.strip()
        hashed_key = hashlib.sha256(api_key.encode()).hexdigest()
        result = await asyncio.to_thread(
            lambda: supabase.table("api_keys").select("user_id").eq("key_hash", hashed_key).eq("is_active", True).execute()
        )
        if result.data:
            user_id = result.data[0]["user_id"]
            auth_info = {"id": user_id, "is_test": api_key.startswith("sk_test_"), "auth_type": "api_key"}
            log_event(level="info", action="auth_success", request_id=request_id, user_id=user_id, auth_type="api_key", key_hash=hashed_key)
        else:
            log_event(level="warning", action="auth_failed", code="INVALID_API_KEY", request_id=request_id, user_id=user_id, key_hash=hashed_key)
            raise_api_error(
                request=request,
                status_code=status.HTTP_401_UNAUTHORIZED,
                error_code="INVALID_API_KEY",
                message="The provided API key is invalid or inactive.",
                recommendation="Verify that your API key is correctly passed in the 'X-API-KEY' header, or generate a new one from your dashboard."
            )
    # get user id from provided jwt
    elif credentials:
        try:
            user_response = await asyncio.to_thread(
                lambda: supabase.auth.get_user(credentials.credentials)
            )
            if not user_response or not getattr(user_response, "user", None):
                log_event(level="warning", action="auth_failed", code="INVALID_JWT", request_id=request_id, endpoint=target_endpoint)
                raise_api_error(
                    request=request,
                    status_code=status.HTTP_401_UNAUTHORIZED,
                    error_code="INVALID_JWT",
                    message="Your authentication token is invalid or expired.",
                    recommendation="Ensure you are passing a valid, active Bearer token in the 'Authorization' header. If your session expired, you will need to log in again to generate a new token."
                )
            user_id = user_response.user.id

            if not user_response.user.email_confirmed_at:
                log_event(level="warning", action="auth_failed", code="EMAIL_NOT_CONFIRMED", request_id=request_id, user_id=user_id)
                raise_api_error(
                    request=request,
                    status_code=status.HTTP_403_FORBIDDEN,
                    error_code="EMAIL_NOT_CONFIRMED",
                    message="Your email address has not been verified.",
                    recommendation="Click the confirmation link we sent to your inbox, or request a new verification email to continue."
                )

            auth_info = {"id": user_id, "is_test": False, "auth_type": "jwt", "provider": user_response.user.app_metadata.get("provider")}

            log_event(level="info", action="auth_success", request_id=request_id, user_id=user_id, auth_type="jwt")
        except Exception as e:
            log_event(level="error", action="jwt_validation_exception", code="AUTH_SYSTEM_ERROR", request_id=request_id, error=str(e), endpoint=target_endpoint)
            raise_api_error(
                request=request,
                status_code=status.HTTP_401_UNAUTHORIZED,
                error_code="INVALID_JWT",
                message="We could not validate your authentication token.",
                recommendation="Your token may be invalid, expired, or our authentication provider is temporarily unreachable. Please log in again."
            )
    
    # user found
    if user_id:
        profile_res = await asyncio.to_thread(
            lambda: supabase.table("profiles").select("*").eq("id", user_id).maybe_single().execute()
        )
        if not profile_res or (hasattr(profile_res, "error") and profile_res.error):
            db_error = str(profile_res.error) if hasattr(profile_res, "error") and profile_res.error else "Unknown DB Error"
            log_event(level="error", action="profile_fetch_failed", code="DATABASE_ERROR", request_id=request_id, user_id=user_id, error=db_error)
            raise_api_error(
                request=request,
                status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
                error_code="DATABASE_ERROR",
                message="We encountered an unexpected error while retrieving your account profile.",
                recommendation="Our database is temporarily unreachable. Please wait a moment and try your request again. If you are making automated requests, we recommend implementing a short delay before retrying."
            )
        
        profile = getattr(profile_res, "data", None)
        # profile not found for this user, check handle_new_user trigger log
        if not profile:
            log_event(level="error", action="profile_resolution_failed", code="PROFILE_NOT_FOUND", request_id=request_id, user_id=user_id, possible_reason="handle_new_user trigger failed")
            raise_api_error(
                request=request,
                status_code=status.HTTP_404_NOT_FOUND,
                error_code="PROFILE_NOT_FOUND",
                message="User profile not found.",
                recommendation="We couldn't locate the account details linked to these credentials. Please log into the dashboard to verify your account status, or contact support for help."
            )
        
        # user is waiting for approval
        if not profile.get("is_allowed", False):
            log_event(level="warning", action="auth_rejected", code="WAITLIST_REQUIRED", request_id=request_id, user_id=user_id)
            raise_api_error(
                request=request,
                status_code=status.HTTP_403_FORBIDDEN,
                error_code="WAITLIST_REQUIRED",
                message="Your account is currently on the waitlist.",
                recommendation="Your profile is set up, but your beta access hasn't been activated yet. Please keep an eye on your inbox for an invitation email."
            )
        
        # user can login
        ctx_user_id.set(user_id)
        auth_info["profile"] = profile
        return auth_info

    # api key or jwt not provided
    log_event(level="warning", action="auth_rejected", code="UNAUTHORIZED", request_id=request_id)
    raise_api_error(
        request=request,
        status_code=status.HTTP_401_UNAUTHORIZED,
        error_code="UNAUTHORIZED",
        message="Authentication credentials were not provided.",
        recommendation="Include a valid JWT in the 'Authorization' header or an API key in the 'X-API-KEY' header to access relevant endpoint."
    )

